import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // üîß Scanner to take input from the user
        Scanner scanner = new Scanner(System.in);
        /*
        ============================================================================================
        üìò JAVA IF-ELSE STATEMENTS ‚Äì COMPLETE THEORY GUIDE (Beginner ‚Üí Advanced)
        ============================================================================================

        üß† WHAT IS A CONDITIONAL STATEMENT?

        A conditional statement lets your program **make decisions** based on certain conditions.

        ‚ñ∏ It's like giving your code a brain: "IF this happens, THEN do that... ELSE do something else."

        ‚ñ∏ Most real-world programs rely heavily on decision-making:
            - Logging in a user (if password matches)
            - Showing a warning (if battery is low)
            - Calculating grades (if score > 90 then A, else B, etc.)
            - Changing UI theme (if nightMode = true)

        ============================================================================================
        üî∞ BASIC SYNTAX ‚Äì IF STATEMENT
        ============================================================================================

        if (condition) {
            // block of code that runs if condition is TRUE
        }

        ‚ñ∏ The condition is a **boolean expression** that evaluates to either true or false.
        ‚ñ∏ If the condition is true, the code inside the block is executed.
        ‚ñ∏ If the condition is false, the block is skipped.

        --------------------------------------------------------------------------------------------
        ‚úÖ Example:
        int age = 18;

        if (age >= 18) {
            // This block runs because 18 >= 18 is true
            System.out.println("You are eligible to vote.");
        }
        --------------------------------------------------------------------------------------------

        ============================================================================================
        üîÑ IF-ELSE STATEMENT
        ============================================================================================

        if (condition) {
            // runs if condition is true
        } else {
            // runs if condition is false
        }

        ‚ñ∏ Use `else` to handle the opposite or fallback case.

        --------------------------------------------------------------------------------------------
        ‚úÖ Example:
        int age = 15;

        if (age >= 18) {
            System.out.println("You are eligible to vote.");
        } else {
            System.out.println("You are not eligible yet.");
        }
        --------------------------------------------------------------------------------------------

        ============================================================================================
        üîó IF - ELSE IF - ELSE LADDER
        ============================================================================================

        Use this when you have multiple **mutually exclusive** conditions to check.

        if (condition1) {
            // runs if condition1 is true
        } else if (condition2) {
            // runs if condition2 is true
        } else if (condition3) {
            // ...
        } else {
            // runs if none of the above conditions are true
        }

        ‚ñ∏ The first true condition wins; others are ignored (short-circuiting).

        --------------------------------------------------------------------------------------------
        ‚úÖ Example:
        int marks = 75;

        if (marks >= 90) {
            System.out.println("Grade A");
        } else if (marks >= 75) {
            System.out.println("Grade B");
        } else if (marks >= 60) {
            System.out.println("Grade C");
        } else {
            System.out.println("Grade D");
        }
        --------------------------------------------------------------------------------------------

        ============================================================================================
        ‚ö†Ô∏è IMPORTANT RULES & PITFALLS
        ============================================================================================

        ‚ñ∏ Only one block runs in an if-else-if ladder.
        ‚ñ∏ Conditions must result in a boolean (`true` or `false`). You can‚Äôt write: if (5) ‚ùå
        ‚ñ∏ Always use **curly braces `{}`**, even if you write only 1 line. Prevents logic errors.
        ‚ñ∏ Use indentation properly for readability.
        ‚ñ∏ Avoid writing deeply nested `if-else` chains ‚Äî extract logic into methods when needed.

        ============================================================================================
        ‚úÖ BOOLEAN EXPRESSIONS & OPERATORS
        ============================================================================================

        You can use comparison and logical operators inside `if` conditions:

        ‚ñ∏ Comparison:
            ==   ‚Üí equals
            !=   ‚Üí not equal
            >    ‚Üí greater than
            <    ‚Üí less than
            >=   ‚Üí greater than or equal to
            <=   ‚Üí less than or equal to

        ‚ñ∏ Logical:
            &&   ‚Üí AND (both must be true)
            ||   ‚Üí OR (at least one must be true)
            !    ‚Üí NOT (inverts true/false)

        --------------------------------------------------------------------------------------------
        ‚úÖ Example:
        int age = 20;
        boolean hasID = true;

        if (age >= 18 && hasID) {
            System.out.println("Access granted.");
        } else {
            System.out.println("Access denied.");
        }
        --------------------------------------------------------------------------------------------

        ============================================================================================
        üß† REAL WORLD USE CASES
        ============================================================================================

        ‚ñ∏ Login authentication
        ‚ñ∏ Showing/hiding UI elements
        ‚ñ∏ Game logic (e.g., if playerHealth == 0 ‚Üí Game Over)
        ‚ñ∏ Calculating taxes or discounts
        ‚ñ∏ Weather forecast logic (e.g., if temperature > 30 ‚Üí show ‚ÄúHot!‚Äù)

        ============================================================================================
        üìä ADVANCED: NESTED IF STATEMENTS
        ============================================================================================

        You can place an `if` inside another `if`. Use only when necessary ‚Äî too much nesting can reduce readability.

        --------------------------------------------------------------------------------------------
        ‚úÖ Example:
        if (loggedIn) {
            if (isAdmin) {
                System.out.println("Welcome Admin.");
            } else {
                System.out.println("Welcome User.");
            }
        } else {
            System.out.println("Please log in first.");
        }
        --------------------------------------------------------------------------------------------

        ============================================================================================
        üßæ COMPARISON VS ASSIGNMENT ‚Äì COMMON BUG
        ============================================================================================

        ‚ö†Ô∏è Mistake: if (x = 5) ‚ùå (This assigns 5 to x instead of comparing)
        ‚úÖ Correct: if (x == 5)

        Use `==` to compare values.
        Use `=` to assign values.

        ============================================================================================
        üìå SUMMARY ‚Äì WHEN TO USE WHAT
        ============================================================================================

        ‚ñ∏ Use `if` for one condition
        ‚ñ∏ Use `if-else` when you want two possibilities (either/or)
        ‚ñ∏ Use `if-else-if` when you want multiple mutually exclusive outcomes
        ‚ñ∏ Use `nested if` when decisions depend on other decisions

        ============================================================================================
        üß™ MINI PRACTICE CHALLENGES (in your head or next file)
        ============================================================================================

        1. Check if a number is even or odd
        2. Determine if a user is eligible for driving license based on age and test score
        3. Assign grades based on marks (A, B, C, etc.)
        4. Build a simple login check using `if` with username/password

        ============================================================================================
        üß† DEEP INSIGHT ‚Äì HOW JAVA PROCESSES IF-ELSE
        ============================================================================================

        Internally, Java evaluates the condition in the parentheses:
        - If the result is `true`, it runs the corresponding block.
        - If not, it moves to the next `else if` or final `else`.

        This logic is fundamental to control flow and forms the basis for advanced topics like:
        - Loops
        - Switch statements
        - Exception handling
        - Functional programming (lambdas, predicates)

        ============================================================================================
        ‚úÖ END OF IF-ELSE THEORY GUIDE
        ============================================================================================
        */

/*
        ================================================================================
        üéØ IF-ELSE STATEMENT PRACTICE ‚Äì Beautified & Commented Walkthrough
        --------------------------------------------------------------------
        ‚úîÔ∏è Decision making using conditions
        ‚úîÔ∏è Grouped input validation
        ‚úîÔ∏è Handling user identity and age-based output
        ‚úîÔ∏è Using booleans for logic control
        ‚úîÔ∏è Added emojis & visual cues for fun learning üß†
        ================================================================================
        */

        // üîπ GROUP 1 ‚Äì IF-ELSE STRUCTURE WITH FIXED AGE
        int age = 70;

        /*
         * üìå We check conditions from top to bottom.
         * Java runs the FIRST matching condition and skips the rest.
         * That's why `age >= 18` runs first before `age >= 65`, even when age = 70.
         */
        if (age >= 18) {
            System.out.println("‚úÖ You are an adult!");
        } else if (age < 0) {
            System.out.println("‚ùå You haven't been born yet!");
        } else if (age == 0) {
            System.out.println("üë∂ You are a baby!");
        } else if (age >= 65) {
            System.out.println("üßì You are a senior!");
        } else {
            System.out.println("üßí You are a child!");
        }

        // üîπ GROUP 2 ‚Äì USER INPUT: NAME & STUDENT STATUS

        String name;

        System.out.print("\nüë§ Enter your name: ");
        name = scanner.nextLine(); // Reads full name including spaces

        boolean isStudent;

        System.out.print("üéì Are you a student? (true/false): ");
        isStudent = scanner.nextBoolean();

        // üß™ GROUP 2.1 ‚Äì NAME VALIDATION
        if (name.isEmpty()) {
            System.out.println("‚ö†Ô∏è You didn't enter your name! üòæ");
        } else {
            System.out.println("üëã Hello " + name + "! üòÉ");
        }

        // üîπ GROUP 3 ‚Äì AGE-BASED CATEGORY SYSTEM

        int inputAge;

        System.out.print("\nüìÖ Please enter your age: ");
        inputAge = scanner.nextInt();

        // üåü Age categories with emoji indicators
        if (inputAge >= 65) {
            System.out.println("üßì You are a senior!");
        } else if (inputAge >= 18) {
            System.out.println("‚úÖ You are an adult!");
        } else if (inputAge >= 13) {
            System.out.println("üßë You are a teenager!");
        } else if (inputAge >= 5) {
            System.out.println("üßí You are a child!");
        } else if (inputAge == 0) {
            System.out.println("üë∂ You are a baby!");
        } else {
            System.out.println("‚ùå You are not even born yet!");
        }

        // üîπ GROUP 4 ‚Äì BOOLEAN CHECK FOR STUDENT STATUS

        if (isStudent) {
            System.out.println("üéì You are a student!");
        } else {
            System.out.println("üìõ You are not a student.");
        }

        // üõë Good practice: Close Scanner at the end
        scanner.close();

        /*
        =================================================================================
        üìò FINAL NOTES:
        ---------------------------------------------------------------------------------
        üß† Java checks if-else conditions top to bottom.
        ‚úÖ Always write most specific checks lower and broad/general checks higher.
        ‚ö†Ô∏è Always use curly braces `{}` to prevent logic bugs.
        ü§ì Use input validation for user entries (like name, numbers, etc.).
        üßº Use comments and spacing to make your code clean and understandable!
        =================================================================================
        */
    }
}
